package day21_ArrayUtility;

public class ForEachLoopIntro {

    public static void main(String[] args) {

        int [] nums = {10,20,30};

        for (int i = 0; i < nums.length; i++)
        {

            int eachNum = nums[i];
            System.out.println(eachNum);

        }

        System.out.println("----------------------------");









    }



}
