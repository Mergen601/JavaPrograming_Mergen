package day06_PrimitiveTypeCastings;

public class AtithmaticOperators
{
    public static void main(String[] args)

    {
        System.out.println("30"+1); //Concatenation

        System.out.println(30 +1); //Addition

        System.out.println(30%2); //Remainder

        System.out.println(30*2);//multiplication


        System.out.println(30/2);//Division

        System.out.println("-----------------------------------");


        System.out.println(100/3); //Result = 33;
        System.out.println(10/0.4); //Result = 25.0;
        System.out.println(10d/4);


    }

}
