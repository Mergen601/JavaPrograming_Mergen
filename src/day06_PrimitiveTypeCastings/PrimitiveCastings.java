package day06_PrimitiveTypeCastings;

public class PrimitiveCastings
{
    public static void main(String[] args)
    {
        byte a = 100;
        short b = a;  // short b = (short)a;//
        int c = b;   //int c = (short)c;//



        long Y = 1000000;
        short z = (short) Y;
        System.out.println(Y+ ":" +z);











    }
}
